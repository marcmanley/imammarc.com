        <h3>The Ideal Teacher of Literature</h3>
        <h4>by Cicero Bruce</h4>

        <p class="drop-cap">With his students, the ideal teacher of literature seeks the center of knowledge, which he understands in
          metaphysical or theological terms. His vehicle is poetry, by which he means the imaginative creation of action
          and character in either prose or verse. When they seek the center in communion with the greatest of poets,
          teacher and student alike transcend the mutable world and begin to grasp, according to Paul Elmer More, "in
          a single firm vision, so to speak, the long course of human history" and to distinguish "what is essential
          therein from what is ephemeral." By seeking the center with his students and affirming its existence, the
          ideal teacher of literature fulfills his duty to the humanities and realizes his calling to guard that in
          which all the humane disciplines have their first and final cause, namely, the Word.</p>

        <p>Noble though some might deem his vocation, the ideal teacher of literature is an anomaly in contemporary
          education. The reason is obvious: education in our time is governed by the sciolistic principles of
          liberalism, which are fundamentally irreconcilable with the Word. James Burnham defined these principles
          nearly sixty ago in his book Suicide of the West: An Essay on the
          Meaning and Destiny of Liberalism. “By liberal principles strictly applied,” he wrote, “the specific function
          of education is to overcome ignorance; and ignorance is overcome by, and only by, acquiring rational,
          scientific knowledge.”</p>

        <p>At the heart of liberalism's educational objective is what the historian Jeffrey Burton Russell has referred
          to in his 2006 book, Paradise Mislaid: How We Lost Heaven and How We Can Regain It, as "physicalism," an
          intellectual bias that began secularizing the humanities about the time of the Reformation. Up to then,
          philosophers in the tradition of Plato and Aristotle acknowledged the subsistence in things of intrinsic
          qualities, which they called essences. Essence is that which abides or, in the case of the human soul,
          transcends and endures eternally. More than Copernicus's supplanting of Ptolemaic cosmography, observes
          Russell, the shift from essentialist to quantitative thought "disenchanted the cosmos" and ultimately
          incapacitated the popular mind for thinking about the anagogical significance of life and death. This
          unfortunate depravation, this failure of the religious attitude, is all too apparent to the ideal teacher of
          literature, who confronts it daily in his classroom.</p>

        <p>What makes the ideal teacher of literature professionally anomalous, if nor downright irritating to the
          fashionable physicalists ensconced in latter-day academia, is his espousal of what Burnham describes as "the
          myriad beliefs that liberalism regards as non-rational or irrational...the debris of superstition, prejudice,
          intuition, habit and custom." According to the tenets of liberalism, such "debris," or what the ideal teacher
          of literature refers to as tradition, should "be admitted to the curriculum only as miscellaneous data to be
          studied objectively by psychology, history, anthropology and the social sciences."</p>

        <p>As liberal educationists see it, tradition is something burdensome to be overcome, nor something alive and
          edifying when updated by the right orthodoxy, as T.S. Eliot argued unabashedly in his 1933 Page-Barbour
          lectures at the University of Virginia. The liberal animus toward tradition is exemplified by Eliot's dubious
          friend Bertrand Russell, who insisted that educational objectives “should be inspired, not by a regretful
          hankering after the extinct beauties of Greece and the Renaissance, but by a shining vision of the society
          that is to be, of the triumph that thought [pure reason shorn of religious faith] will achieve in the time to
          come.”</p>

        <p>Liberalism's suspicious attitude toward those who would teach poetry as a way of knowing truth through its embodiment in beauty is apparent in Russell's remarks. It is implicit in the writings of Francis Bacon, Russell's intellectual ancestor. In Thomas Babington Macaulay's nineteenth-century essay on Milton, it is clearly pronounced: "As the light of knowledge breaks in upon its exhibitions, as the outlines of certainty become more and more definite and the shades of probability more and more distinct, the hues and lineaments of the phantoms which the poet calls up grow fainter and fainter." Macaulay presents poetry precisely as the liberal would have it presented in contemporary education: as "a magic lantern" that produced comforting illusions “in a dark age," beyond which man has moved into the (seemingly) salvific illumination of Comtian positivism.</p>

