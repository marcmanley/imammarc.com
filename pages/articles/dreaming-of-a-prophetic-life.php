    <p class="pages date">July 2023</p>
    <img class="qualifications-img" src="../../media/imgs/dreaming-of-a-prophetic-life.png" alt="Thumbnail of an orange tree from Imam Marc's backyard">
    <blockquote class="clementine-quote drop-cap">“I didn’t tell a lot of people what I was doing here. Like, for instance I bought my house back in Michigan from the police chief and he stopped by and he said ’oh, I see you’re moving’. And I said, ‘yeah’, and he goes ‘what are you doing?’, and I told him what I was doing and he was like, ‘oh my God, I always wanted to do that!’.” &mdash; Dan Schultz</blockquote>
    <p class="pages">Perhaps it’s all just romance and nostalgia or perhaps it’s the fiṭrah crying out to free itself of this modern contagion we call civilization.</p>
    <p class="pages">I have spent enough time on farms to know that there’s little room for nostalgia or romance if one is going to survive. The work can be arduous to say the least though this fact in no way preempts it being a deeply satisfying way of living. But there is a truth to what Dan Schultz, the owner of a 100-acre village he started near the California-Oregon border, has to say after leaving his life in the Flint, Michigan area,</p>
    <blockquote class="clementine-quote">“I bet at least five or six out of ten people, when they know what it is we’re doing here, go ‘oh my God, that’s my dream’…it’s truly a zeitgeist.”</blockquote>
    <p class="pages">In my estimation, the vast majority of attempts to live outside of mainstream urban areas have been plagued by unrealistic capacities to support family life in such remote locations, at best, and cultish mentalities at worst. And yet despite these very real obstacles I cannot help but look at examples like Dan Schultz and pine for such an existence. I am reminded of Peter Jackson's Galadriel in the prologue to his 2001 adaptation of J. R. R. Tolkien's <em>The Lord of the Rings: The Fellowship of the Ring</em> in which Galadriel states,</p>
    <blockquote class="clementine-quote">“The world is changed. I feel it in the water. I feel it in the earth. I smell it in the air.”</blockquote>
    <p class="pages">And of course there is the hadith of the Prophet ‎ﷺ in which he states,</p>
    <blockquote class="arabic-normal">يوشك أن يكون خير مال المسلم غنم يتبع بها شعف الجبال ومواضع القطر يفر بدينه من الفتن - صحيح البخاري الحديث التاسع عشر من كتاب الإيمان</blockquote>
    <blockquote class="clementine-quote">"A time will soon come when the best property of a Muslim will be sheep which he will take on the top of mountains and the places of rainfall, fleeing with his religion from afflictions and calamities." &mdash; <strong>Sahih al-Bukhari, hadith no. 19 from the Book of Faith</strong></blockquote>
    <p class="pages">And so I suppose I, we, will just have to content ourselves to dream a bit longer.</p>
