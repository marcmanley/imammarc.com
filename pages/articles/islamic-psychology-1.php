      <blockquote class="clementine-quote drop-cap">“Cultural sociologist Eva Illouz (2008) makes the argument that a therapeutic ethos now transcends all national borders and regional differences, hereby providing the basis for a global psychological mentality and discourse about individuality. She claims that no other cultural framework, with the exception of political liberalism and market liberalism's economic logic of profit, has exercised as much influence on the twentieth century's ideas about the individual. The Danish psychologist Svend Brinkmann (2008) has stated that modern psychology has become so predominant in our understanding of ourselves and the surrounding world that it constitutes what the philosopher Charles Taylor (2004) has called a social imaginary, which is not only a specific theory espoused by many about the nature of the world, but the very framework comprising our theories about ourselves and the world.” &mdash; taken from <strong>Ole Jacob Madsen's</strong>, <em>The Therapeutic Turn: How Psychology Altered Western Culture</em>, Routledge (2014)</blockquote>
      <p class="pages">Increasingly our society moves towards the psychological and away from the theological. This is no less true amongst Muslims in the West who’ve striven to develop an “Islāmic psychology” but I ask this with all good intentions: how are Muslims psychologists going to reconcile the ever-increasing tendency to view human experience exclusively through the lens of the psychological, an ideology rooted, if not in the denial of the soul (though that too is on the rise), certainly an agnosticism towards its existence, and Islām, whose primary sources speak of a human condition dominated by “impurity” and a darkness of the human heart. Personally, and I know many Muslims striving in this field &mdash; some even dear friends, I have not sufficiently seen how Muslims are tackling this divergent view of the human condition in any satisfactory way.</p>
      <p class="pages">To me, this is why modern secular psychology remains not only largely ineffective in addressing the human condition, not merely from a diagnostic point of view, but simply, as a Muslim, modern psychology does not understand &mdash; from a point of departure &mdash; what a human being is. And while we may debate this essence as religious/non-religious, I cannot however place much trust in a so-called Islāmic psychology (at least as of writing this article) any more than I can a secular one.</p>
      <blockquote class="clementine-quote">"If the dominant character type of the twentieth century is really what [Philip] Rieff calls 'psychological man,' the consequences for western society are quite incalculable." &mdash; <strong>Gordon Wright and Arthur Mejia, Jr. (ed.)</strong>, <em>An Age of Controversy: Discussion Problems in Twentieth-Century European History</em> (1963). Quoted in <strong>Philip Rieff's</strong> <em>The Triumph of the Therapeutic: Uses of Faith After Freud</em> (1966)</blockquote>
      <p class="pages">I leave you with the following quote from one of the founding fathers of modern psychology, Granville Stanley Hall [1844—1924], to ponder while I ask one last question: <em>"Did Islamic psychology lose its soul along the way?"</em></p>
